"""Решение квадратного уравнения"""
a = float(input("Введите коэффициент а:"))
b = float(input("Введите коэффициент b:"))
c = float(input("Введите коэффициент c:"))
D = b**2 - 4 * a * c

if D > 0:
    x1 = (-b + D**0.5) / 2 * a

    x2 = (-b - D**0.5) / 2 * a

    print(f"Корни:{x1}, {x2}")

elif D == 0:
    pass

else:
    print("Нет корней")
